public class ObjectOf2Strings {
    String EN;
    String Context;

    public String getContext() {
        return Context;
    }

    public void setContext(String Context) {
        this.Context = Context;
    }

    public String getEN() {
        return EN;
    }

    public void setEN(String EN) {
        this.EN = EN;
    }

    public ObjectOf2Strings() {
    }

    public ObjectOf2Strings(String EN, String Context) {
        this.EN = EN;
        this.Context = Context;
    }
    
    
    
}
